#!/bin/bash
# step4-safe-remove-general-info.sh - –±–µ–∑–æ–ø–∞—Å–Ω–æ–µ —É–¥–∞–ª–µ–Ω–∏–µ –±–ª–æ–∫–∞

echo "=== –ë–ï–ó–û–ü–ê–°–ù–û–ï –£–î–ê–õ–ï–ù–ò–ï –ë–õ–û–ö–ê '–û–ë–©–ê–Ø –ò–ù–§–û–†–ú–ê–¶–ò–Ø' ==="
echo "–í–µ—Ä—Å–∏—è: 1.0.0"
echo "–î–∞—Ç–∞: $(date)"
echo ""

cd /Users/kirillkravcov/obsidian/my-digital-garden-content/uch-docs || exit 1

# –°–æ–∑–¥–∞–µ–º backup –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é
TIMESTAMP=$(date +%Y%m%d-%H%M%S)
BACKUP_DIR="backup-before-remove-general-info-$TIMESTAMP"
mkdir -p "$BACKUP_DIR"

echo "üìÅ –°–æ–∑–¥–∞—é backup –≤: $BACKUP_DIR"
echo ""

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ —É–¥–∞–ª–µ–Ω–∏—è –±–ª–æ–∫–∞
remove_general_info_block() {
    local file="$1"
    local backup_file="$BACKUP_DIR/$(basename "$file")"
    
    # –°–æ–∑–¥–∞–µ–º backup
    cp "$file" "$backup_file"
    
    # –°–æ–∑–¥–∞–µ–º –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Ñ–∞–π–ª
    local temp_file="${file}.tmp.$$"
    
    # –£–¥–∞–ª—è–µ–º –±–ª–æ–∫ "–û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è" –∏—Å–ø–æ–ª—å–∑—É—è awk
    # –ë–ª–æ–∫ –∏–¥–µ—Ç –æ—Ç –∑–∞–≥–æ–ª–æ–≤–∫–∞ –¥–æ —Å–ª–µ–¥—É—é—â–µ–≥–æ –∑–∞–≥–æ–ª–æ–≤–∫–∞ —Ç–æ–≥–æ –∂–µ –∏–ª–∏ –±–æ–ª–µ–µ –≤—ã—Å–æ–∫–æ–≥–æ —É—Ä–æ–≤–Ω—è
    awk '
    BEGIN {
        in_block = 0
        block_started = 0
        header_level = 0
    }
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —É—Ä–æ–≤–µ–Ω—å –∑–∞–≥–æ–ª–æ–≤–∫–∞
    function get_header_level(line) {
        if (match(line, /^#+/)) {
            return RLENGTH
        }
        return 0
    }
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º "–û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è"
    function is_general_info_header(line) {
        return match(tolower(line), /^#+\s+.*–æ–±—â–∞—è\s+–∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è/)
    }
    
    # –û—Å–Ω–æ–≤–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞
    {
        current_level = get_header_level($0)
        
        if (is_general_info_header($0)) {
            # –ù–∞—á–∞–ª–æ –±–ª–æ–∫–∞ "–û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è"
            in_block = 1
            block_started = 1
            header_level = current_level
            next  # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º —ç—Ç—É —Å—Ç—Ä–æ–∫—É (–Ω–µ –ø–µ—á–∞—Ç–∞–µ–º)
        }
        else if (in_block && current_level > 0) {
            # –ù–∞—à–ª–∏ –¥—Ä—É–≥–æ–π –∑–∞–≥–æ–ª–æ–≤–æ–∫
            if (current_level <= header_level) {
                # –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–æ–≥–æ –∂–µ –∏–ª–∏ –±–æ–ª–µ–µ –≤—ã—Å–æ–∫–æ–≥–æ —É—Ä–æ–≤–Ω—è - –∫–æ–Ω–µ—Ü –±–ª–æ–∫–∞
                in_block = 0
            }
            # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π –∑–∞–≥–æ–ª–æ–≤–æ–∫ –±–æ–ª–µ–µ –Ω–∏–∑–∫–æ–≥–æ —É—Ä–æ–≤–Ω—è, –æ—Å—Ç–∞–µ–º—Å—è –≤ –±–ª–æ–∫–µ
        }
        
        if (!in_block) {
            # –ü–µ—á–∞—Ç–∞–µ–º —Å—Ç—Ä–æ–∫–∏ –≤–Ω–µ –±–ª–æ–∫–∞
            print
        }
        else if (block_started) {
            # –ú—ã –≤–Ω—É—Ç—Ä–∏ –±–ª–æ–∫–∞, –Ω–æ –Ω–µ –ø–µ—á–∞—Ç–∞–µ–º –µ–≥–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ
            # –ü—Ä–æ—Å—Ç–æ –ø—Ä–æ–ø—É—Å–∫–∞–µ–º —Å—Ç—Ä–æ–∫–∏
            next
        }
    }
    
    END {
        if (block_started) {
            print "// –ë–ª–æ–∫ \"–û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è\" —É–¥–∞–ª–µ–Ω" > "/dev/stderr"
        }
    }
    ' "$file" > "$temp_file"
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∏–∑–º–µ–Ω–µ–Ω–∏—è
    if ! cmp -s "$file" "$temp_file"; then
        # –§–∞–π–ª –∏–∑–º–µ–Ω–∏–ª—Å—è
        mv "$temp_file" "$file"
        echo "‚úÖ –£–¥–∞–ª–µ–Ω –±–ª–æ–∫ –≤: $(basename "$file")"
        return 0
    else
        # –§–∞–π–ª –Ω–µ –∏–∑–º–µ–Ω–∏–ª—Å—è
        rm "$temp_file"
        echo "‚ö†Ô∏è  –ë–ª–æ–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω –∏–ª–∏ —É–∂–µ —É–¥–∞–ª–µ–Ω –≤: $(basename "$file")"
        return 1
    fi
}

# –¢–ï–°–¢–û–í–´–ô –†–ï–ñ–ò–ú: —Å–Ω–∞—á–∞–ª–∞ –ø–æ–∫–∞–∂–µ–º —á—Ç–æ –±—É–¥–µ—Ç —É–¥–∞–ª–µ–Ω–æ
echo "üîç –¢–ï–°–¢–û–í–´–ô –†–ï–ñ–ò–ú: –ø—Ä–æ–≤–µ—Ä–∫–∞ 3 —Ñ–∞–π–ª–æ–≤"
echo "------------------------------------"
echo ""

# –¢–µ—Å—Ç–∏—Ä—É–µ–º –Ω–∞ 3 —Ñ–∞–π–ª–∞—Ö —Å —Ä–∞–∑–Ω—ã–º–∏ —Ñ–æ—Ä–º–∞—Ç–∞–º–∏
TEST_FILES=(
    "00-04 - –õ–∏–Ω–∏—è –ì. –î–æ–∫—É–º–µ–Ω—Ç–∞—Ü–∏—è.md"          # #### –û–ë–©–ê–Ø –ò–ù–§–û–†–ú–ê–¶–ò–Ø
    "00-03 - –õ–∏–Ω–∏—è –í. –ü—Ä–æ—Ü–µ—Å—Å—ã.md"              # ### –û–ë–©–ê–Ø –ò–ù–§–û–†–ú–ê–¶–ò–Ø
    "00 - UCH.md"                               # ## üìã –û–ë–©–ê–Ø –ò–ù–§–û–†–ú–ê–¶–ò–Ø
)

CHANGED_COUNT=0
TOTAL_TESTED=0

for test_file in "${TEST_FILES[@]}"; do
    if [ -f "$test_file" ]; then
        TOTAL_TESTED=$((TOTAL_TESTED + 1))
        echo "üìÑ –¢–µ—Å—Ç–∏—Ä—É–µ–º: $test_file"
        
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —á—Ç–æ –µ—Å—Ç—å —Å–µ–π—á–∞—Å
        echo "  –î–æ —É–¥–∞–ª–µ–Ω–∏—è (–ø–µ—Ä–≤—ã–µ 5 —Å—Ç—Ä–æ–∫ –ø–æ—Å–ª–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞):"
        # –ù–∞—Ö–æ–¥–∏–º —Å—Ç—Ä–æ–∫—É —Å "–û–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è"
        line_num=$(grep -n -i "–æ–±—â–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è" "$test_file" | head -1 | cut -d: -f1)
        if [ -n "$line_num" ]; then
            start=$((line_num + 1))
            end=$((line_num + 5))
            sed -n "${start},${end}p" "$test_file" | sed 's/^/    > /'
        fi
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º —É–¥–∞–ª–µ–Ω–∏–µ
        if remove_general_info_block "$test_file"; then
            CHANGED_COUNT=$((CHANGED_COUNT + 1))
            
            # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —á—Ç–æ —Å—Ç–∞–ª–æ
            echo "  –ü–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è (—Ç–µ –∂–µ —Å—Ç—Ä–æ–∫–∏):"
            if [ -n "$line_num" ]; then
                # –ò—â–µ–º —Ç–æ—Ç –∂–µ –¥–∏–∞–ø–∞–∑–æ–Ω
                sed -n "${start},${end}p" "$test_file" 2>/dev/null | sed 's/^/    > /' || echo "    (–±–ª–æ–∫ —É–¥–∞–ª–µ–Ω)"
            fi
        fi
        
        echo "---"
    fi
done

echo ""
echo "üìä –†–ï–ó–£–õ–¨–¢–ê–¢ –¢–ï–°–¢–ê:"
echo "  –ü—Ä–æ—Ç–µ—Å—Ç–∏—Ä–æ–≤–∞–Ω–æ: $TOTAL_TESTED —Ñ–∞–π–ª–æ–≤"
echo "  –ò–∑–º–µ–Ω–µ–Ω–æ: $CHANGED_COUNT —Ñ–∞–π–ª–æ–≤"
echo ""

# –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º backup –¥–ª—è —Ç–µ—Å—Ç–æ–≤—ã—Ö —Ñ–∞–π–ª–æ–≤
echo "üîÑ –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—é —Ç–µ—Å—Ç–æ–≤—ã–µ —Ñ–∞–π–ª—ã –∏–∑ backup..."
for test_file in "${TEST_FILES[@]}"; do
    backup_file="$BACKUP_DIR/$(basename "$test_file")"
    if [ -f "$backup_file" ] && [ -f "$test_file" ]; then
        cp "$backup_file" "$test_file"
        echo "  ‚úÖ –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω: $test_file"
    fi
done

echo ""
echo "üöÄ –°–õ–ï–î–£–Æ–©–ò–ô –®–ê–ì:"
echo "  1. –ü—Ä–æ–≤–µ—Ä–∏—Ç—å —á—Ç–æ —Ç–µ—Å—Ç–æ–≤—ã–µ —Ñ–∞–π–ª—ã –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏–ª–∏—Å—å –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ"
echo "  2. –ï—Å–ª–∏ –≤—Å–µ –æ–∫, –∑–∞–ø—É—Å—Ç–∏—Ç—å —É–¥–∞–ª–µ–Ω–∏–µ –¥–ª—è –≤—Å–µ—Ö —Ñ–∞–π–ª–æ–≤"
echo "  3. –ü–æ—Å–ª–µ —É–¥–∞–ª–µ–Ω–∏—è –ø—Ä–æ–≤–µ—Ä–∏—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã"
echo ""
echo "üìã –ö–û–ú–ê–ù–î–ê –î–õ–Ø –ü–û–õ–ù–û–ì–û –£–î–ê–õ–ï–ù–ò–Ø (–ø–æ—Å–ª–µ —Ç–µ—Å—Ç–∞):"
