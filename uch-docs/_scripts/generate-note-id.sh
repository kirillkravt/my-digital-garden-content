#!/bin/bash
# –°–∫—Ä–∏–ø—Ç –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ –Ω–æ–≤—ã—Ö ID –∑–∞–º–µ—Ç–æ–∫ –≤ —Å–∏—Å—Ç–µ–º–µ hex-–Ω—É–º–µ—Ä–∞—Ü–∏–∏

set -e  # –í—ã—Ö–æ–¥ –ø—Ä–∏ –æ—à–∏–±–∫–µ

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Ü–≤–µ—Ç–Ω–æ–≥–æ —Ç–µ–∫—Å—Ç–∞
print_color() {
    echo -e "${1}${2}${NC}"
}

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–æ–≥–æ ID
if [ -z "$1" ]; then
    print_color $YELLOW "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: $0 <parent_id> [note_title]"
    print_color $BLUE "–ü—Ä–∏–º–µ—Ä—ã:"
    print_color $BLUE "  $0 00-02-01 \"–ù–æ–≤–∞—è –∑–∞–¥–∞—á–∞\""
    print_color $BLUE "  $0 00-02 \"–ù–æ–≤—ã–π –∫–æ–º–ø–æ–Ω–µ–Ω—Ç\""
    print_color $BLUE "  $0 \"\" \"–ù–æ–≤—ã–π –ø—Ä–æ–µ–∫—Ç\" (–¥–ª—è –∫–æ—Ä–Ω–µ–≤–æ–≥–æ —É—Ä–æ–≤–Ω—è)"
    exit 1
fi

PARENT_ID="$1"
NOTE_TITLE="$2"
VAULT_PATH="/Users/kirillkravcov/obsidian/my-digital-garden-content/uch-docs"

# –ü–µ—Ä–µ—Ö–æ–¥–∏–º –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é vault
cd "$VAULT_PATH" || {
    print_color $RED "–û—à–∏–±–∫–∞: –ù–µ –º–æ–≥—É –ø–µ—Ä–µ–π—Ç–∏ –≤ $VAULT_PATH"
    exit 1
}

print_color $GREEN "üéØ –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –Ω–æ–≤–æ–≥–æ ID –¥–ª—è —Å–∏—Å—Ç–µ–º—ã –Ω—É–º–µ—Ä–∞—Ü–∏–∏ UCH"
print_color $BLUE "=============================================="

# –ï—Å–ª–∏ –ø—É—Å—Ç–æ–π parent_id - –≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–æ—Ä–Ω–µ–≤–æ–π ID
if [ -z "$PARENT_ID" ]; then
    print_color $YELLOW "–ì–µ–Ω–µ—Ä–∞—Ü–∏—è ID –¥–ª—è –∫–æ—Ä–Ω–µ–≤–æ–≥–æ —É—Ä–æ–≤–Ω—è..."
    
    # –ò—â–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π –∫–æ—Ä–Ω–µ–≤–æ–π ID
    MAX_ROOT=$(find . -maxdepth 1 -name "*.md" -type f \
        | grep -E '^\./[0-9a-fA-F]{2} - .*\.md$' \
        | sed 's|^\./\([0-9a-fA-F]\{2\}\) - .*\.md$|\1|' \
        | sort -r | head -n1)
    
    if [ -z "$MAX_ROOT" ]; then
        NEW_ID="00"
    else
        # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º hex –≤ decimal, —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º, –æ–±—Ä–∞—Ç–Ω–æ –≤ hex
        DEC_VAL=$((16#$MAX_ROOT))
        NEXT_VAL=$((DEC_VAL + 1))
        NEW_ID=$(printf "%02x" $NEXT_VAL)
    fi
    
    NEW_LEVEL=1
else
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–æ—Ä–º–∞—Ç parent_id
    if [[ ! "$PARENT_ID" =~ ^[0-9a-fA-F]{2}(-[0-9a-fA-F]{2})*$ ]]; then
        print_color $RED "–û—à–∏–±–∫–∞: –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç parent_id: $PARENT_ID"
        print_color $YELLOW "–§–æ—Ä–º–∞—Ç –¥–æ–ª–∂–µ–Ω –±—ã—Ç—å: XX-XX-XX (hex, —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å -)"
        exit 1
    fi
    
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —É—Ä–æ–≤–µ–Ω—å –Ω–æ–≤–æ–≥–æ –¥–æ–∫—É–º–µ–Ω—Ç–∞
    PARENT_LEVEL=$((($(echo "$PARENT_ID" | tr -cd '-' | wc -c) / 2) + 1))
    NEW_LEVEL=$((PARENT_LEVEL + 1))
    
    # –ò—â–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã —Å —ç—Ç–∏–º parent_id
    print_color $BLUE "–ü–æ–∏—Å–∫ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö –¥–µ—Ç–µ–π –¥–ª—è $PARENT_ID..."
    
    # –ü–∞—Ç—Ç–µ—Ä–Ω –¥–ª—è –ø–æ–∏—Å–∫–∞ –¥–µ—Ç–µ–π: parent_id-XX
    CHILDREN_PATTERN="${PARENT_ID}-[0-9a-fA-F]\{2\}"
    
    # –ò—â–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π child ID
    MAX_CHILD=$(find . -name "*.md" -type f \
        | grep -E "${CHILDREN_PATTERN} - " \
        | sed "s|.*/${PARENT_ID}-\([0-9a-fA-F]\{2\}\) - .*\.md$|\1|" \
        | sort -r | head -n1)
    
    if [ -z "$MAX_CHILD" ]; then
        NEW_CHILD_ID="01"
    else
        # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º hex –∑–Ω–∞—á–µ–Ω–∏–µ
        DEC_VAL=$((16#$MAX_CHILD))
        NEXT_VAL=$((DEC_VAL + 1))
        NEW_CHILD_ID=$(printf "%02x" $NEXT_VAL)
    fi
    
    NEW_ID="${PARENT_ID}-${NEW_CHILD_ID}"
fi

# –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∏–º—è —Ñ–∞–π–ª–∞
if [ -n "$NOTE_TITLE" ]; then
    FILENAME="${NEW_ID} - ${NOTE_TITLE}.md"
    
    # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º —Ç–µ–≥–∏
    print_color $YELLOW "–í–≤–µ–¥–∏—Ç–µ —Ç–µ–≥–∏ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª (–Ω–∞–ø—Ä–∏–º–µ—Ä: @bug @audio @high-priority):"
    read -r TAGS_INPUT
    
    # –°–æ–∑–¥–∞–µ–º —Ñ–∞–π–ª —Å –±–∞–∑–æ–≤–æ–π —Å—Ç—Ä—É–∫—Ç—É—Ä–æ–π
    cat > "$FILENAME" << FILE_CONTENT
# ${NEW_ID} - ${NOTE_TITLE}

## üìã –û–ë–©–ê–Ø –ò–ù–§–û–†–ú–ê–¶–ò–Ø
- **–°—Ç–∞—Ç—É—Å**: üÜï –ù–û–í–´–ô –î–û–ö–£–ú–ï–ù–¢
- **–†–æ–¥–∏—Ç–µ–ª—å**: ${PARENT_ID:-[–∫–æ—Ä–µ–Ω—å]}
- **ID**: \`${NEW_ID}\`
- **–£—Ä–æ–≤–µ–Ω—å**: ${NEW_LEVEL}
- **–¢–µ–≥–∏**: ${TAGS_INPUT:-@todo}

## üéØ –û–ü–ò–°–ê–ù–ò–ï

*–î–æ–±–∞–≤—å—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∑–¥–µ—Å—å...*

## üìã –ó–ê–î–ê–ß–ò

- [ ] –ó–∞–¥–∞—á–∞ 1
- [ ] –ó–∞–¥–∞—á–∞ 2

## üîó –°–í–Ø–ó–ê–ù–ù–´–ï –î–û–ö–£–ú–ï–ù–¢–´

### –†–û–î–ò–¢–ï–õ–¨–°–ö–ò–ï:
${PARENT_ID:+# - [[${PARENT_ID}]]}

### –î–û–ß–ï–†–ù–ò–ï:
*–ü–æ–∫–∞ –Ω–µ—Ç*

### –°–ú–ï–ñ–ù–´–ï:
*–î–æ–±–∞–≤—å—Ç–µ —Å—Å—ã–ª–∫–∏...*

---
*–°–æ–∑–¥–∞–Ω–æ: $(date +%Y-%m-%d)*
*–°—Ç–∞—Ç—É—Å: –ß–µ—Ä–Ω–æ–≤–∏–∫*
FILE_CONTENT
    
    print_color $GREEN "‚úÖ –°–æ–∑–¥–∞–Ω —Ñ–∞–π–ª: $FILENAME"
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å parent_id, –æ–±–Ω–æ–≤–ª—è–µ–º —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π —Ñ–∞–π–ª
    if [ -n "$PARENT_ID" ] && [ "$PARENT_ID" != "00" ]; then
        PARENT_FILE=$(find . -name "${PARENT_ID} - *.md" -type f | head -n1)
        
        if [ -n "$PARENT_FILE" ]; then
            print_color $BLUE "üìù –û–±–Ω–æ–≤–ª—è—é —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π —Ñ–∞–π–ª: $PARENT_FILE"
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –µ—Å—Ç—å –ª–∏ —É–∂–µ —Å—Å—ã–ª–∫–∞ –Ω–∞ —ç—Ç–æ—Ç –¥–æ–∫—É–º–µ–Ω—Ç
            if ! grep -q "\[\[${NEW_ID} -" "$PARENT_FILE"; then
                # –î–æ–±–∞–≤–ª—è–µ–º –≤ —Ä–∞–∑–¥–µ–ª –¥–æ—á–µ—Ä–Ω–∏—Ö –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤
                if grep -q "### –î–û–ß–ï–†–ù–ò–ï:" "$PARENT_FILE"; then
                    # –í—Å—Ç–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ "### –î–û–ß–ï–†–ù–ò–ï:"
                    sed -i '' "/### –î–û–ß–ï–†–ù–ò–ï:/a\\
- [[${NEW_ID} - ${NOTE_TITLE}]]" "$PARENT_FILE"
                else
                    # –î–æ–±–∞–≤–ª—è–µ–º —Ä–∞–∑–¥–µ–ª
                    echo "" >> "$PARENT_FILE"
                    echo "### –î–û–ß–ï–†–ù–ò–ï:" >> "$PARENT_FILE"
                    echo "- [[${NEW_ID} - ${NOTE_TITLE}]]" >> "$PARENT_FILE"
                fi
                print_color $GREEN "‚úÖ –°—Å—ã–ª–∫–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∞ –≤ —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π –¥–æ–∫—É–º–µ–Ω—Ç"
            fi
        else
            print_color $YELLOW "‚ö†Ô∏è –†–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏–π —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω: $PARENT_ID"
        fi
    fi
else
    # –¢–æ–ª—å–∫–æ –≤—ã–≤–æ–¥ ID –±–µ–∑ —Å–æ–∑–¥–∞–Ω–∏—è —Ñ–∞–π–ª–∞
    print_color $GREEN "üìã –†–µ–∑—É–ª—å—Ç–∞—Ç –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏:"
    print_color $BLUE "  –ù–æ–≤—ã–π ID: ${NEW_ID}"
    print_color $BLUE "  –£—Ä–æ–≤–µ–Ω—å: ${NEW_LEVEL}"
    print_color $BLUE "  –†–æ–¥–∏—Ç–µ–ª—å: ${PARENT_ID:-[–∫–æ—Ä–µ–Ω—å]}"
    print_color $YELLOW ""
    print_color $YELLOW "–ß—Ç–æ–±—ã —Å–æ–∑–¥–∞—Ç—å —Ñ–∞–π–ª, —É–∫–∞–∂–∏—Ç–µ –Ω–∞–∑–≤–∞–Ω–∏–µ:"
    print_color $YELLOW "  $0 \"$PARENT_ID\" \"–ù–∞–∑–≤–∞–Ω–∏–µ –∑–∞–º–µ—Ç–∫–∏\""
fi

print_color $BLUE "=============================================="
print_color $GREEN "üéâ –ì–æ—Ç–æ–≤–æ! –°–∏—Å—Ç–µ–º–∞ hex-–Ω—É–º–µ—Ä–∞—Ü–∏–∏ –∞–∫—Ç–∏–≤–Ω–∞."
